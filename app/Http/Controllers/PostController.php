     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $posts = Post::with('categories','messengerBrand')
        ->orderByDesc('id')
        ->paginate(15);
        return view('groups.index',compact('posts'));
    }

